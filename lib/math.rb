def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  diff= num1-num2
  diff
end

def division(num1, num2)
  div = num1 / num2
  div
end

def multiplication(num1, num2)
  product = num1 * num2
  product
end

def modulo(num1, num2)
  mod = num1 % num2
  mod
end

def square_root(num)
  sq_rt = Maths.sqrt(num)
  sq_rt
end

